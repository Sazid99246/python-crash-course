places = ["Macca", "Medina", "Canada", "Austrelia", "Kolkata"]
print("Original List")
print(places)

print("Sorted list in alphabetical order")
print(sorted(places))

print("Original list is remaining the same")
print(places)

print("Sorted list reversly in alphabetical order")
places.reverse()
print(places)

print("Sorted list again")
places.reverse();
print(places)

print("Sorting the list in alphabetical order again")
places.sort()
print(places)

print("Sorting the list in alphabetical order reversely")
places.sort(reverse=True)
print(places)